$('.accion1').on('click', function() {
  if ($('.accion-info1').is(':visible')) {
    $('.accion-info1').slideUp()
    document.querySelector('.cross').style.transform = 'rotate(0deg)'
  } else {
    $('.accion-info1').slideDown()
    document.querySelector('.cross').style.transform = 'rotate(-45deg)'
    return false
  }
})

